--1번문제
SELECT COUNT(*) 학생수
FROM TB_STUDENT
WHERE DEPARTMENT_NO = '003' AND
          ENTRANCE_DATE LIKE '01%';

--2번문제
SELECT CATEGORY 계열,
          DEPARTMENT_NAME 학과이름,
          CAPACITY 정원
FROM TB_DEPARTMENT
WHERE (CAPACITY BETWEEN 20 AND 30) AND 
           CATEGORY = '공학'
ORDER BY 2;           

--3번문제
SELECT CATEGORY 계열,
          COUNT(*) 학과수
FROM TB_DEPARTMENT
WHERE CATEGORY LIKE '%학%'
GROUP BY CATEGORY
ORDER BY 2 DESC;

